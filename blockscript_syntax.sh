# BlockScript Smart Contract Example
# Simplified syntax for the BlockScript language

# Variable declaration and assignment
let x = 10
let y = 5

# Conditional statement
if (x > y) {
    print("x is greater than y")
} else {
    print("y is greater than or equal to x")
}

# Loop example
let i = 1
while (i <= 5) {
    print(i)
    i = i + 1
}
